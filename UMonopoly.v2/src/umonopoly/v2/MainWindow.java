    private void jb_KupTakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_KupTakActionPerformed
        jd_KupP.setVisible(false);
        if (silnik.getAktywnyPlayer().getMoney()>silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getBuy_out())
        {
        silnik.getPole(silnik.getAktywnyPlayer().getWhere()).setOwner(silnik.getAktywnyPlayer().getId());
        silnik.getAktywnyPlayer().setMoney(silnik.getAktywnyPlayer().getMoney()-silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getBuy_out());
        silnik.getAktywnyPlayer().addToHand(silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getID());
        
        silnik.setLog(silnik.getLog()+"\n"+silnik.getAktywnyPlayer().getName()+" Kupil "+ silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getName());
        
        jl_AktywnyGracz.setText("Aktywny Gracz: "+silnik.getAktywnyPlayer().getName()+" "+silnik.getAktywnyPlayer().getMoney()+"$");
        jta_Fundusze.setText("");
        for(int x=0;x<silnik.getSizeOfPlayer();x++)
            {
                jta_Fundusze.setText(jta_Fundusze.getText()+silnik.getGracze(x).getName()+": "+silnik.getGracze(x).getMoney()+"$\n");
            }
        jtp_Log.setText(silnik.getLog());
        }
        else
        {
            jb_Error.setVisible(true);
        }
    }//GEN-LAST:event_jb_KupTakActionPerformed

