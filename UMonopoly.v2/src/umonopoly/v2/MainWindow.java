    private void jb_RollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_RollActionPerformed
        jb_Roll.setEnabled(false);
        jb_Eot.setEnabled(true);
        silnik.random2k6();
        jtp_Log.setText(silnik.getLog());
        
        if(silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner()!=10)
        {
        if (silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner()==5)
        {
            jl_KupP.setText("Czy chcesz kupic " + silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getName()+"?");
            if(silnik.getAktywnyPlayer().getMoney()<silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getBuy_out())
            {
                jb_KupTak.setEnabled(false);
            }
            jd_KupP.setVisible(true);
            
        }
        
        if(silnik.getAktywnyPlayer().getId()==silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner() && silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getHome()>0.0)
        {
            jl_KupD.setText("Czy chcesz kupic labolatrium na: "+silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getName()+" za "+silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getHome()+"?");
            jd_KupD.setVisible(true);
            if(silnik.getAktywnyPlayer().getMoney()<silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getHome())
            {
                jb_KupDTak.setEnabled(false);
            }
        
        }
        
        if (silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner()!=silnik.getAktywnyPlayer().getId() && silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner()!=5)
       {
            jd_Rachunek.setVisible(true);
            jta_Rachunek.setText("Od: "+silnik.getGracze(silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getOwner()).getName()+"\nDo: "+silnik.getAktywnyPlayer().getName()+"\nZa: "+silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getName()+"\nNależnosc: "+silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getKoszt()+"\nIlosc Labolatoriow:" + silnik.getPole(silnik.getAktywnyPlayer().getWhere()).getDomki());
      }
        }
         jta_Fundusze.setText("");
            for(int x=0;x<silnik.getSizeOfPlayer();x++)
            {
                jta_Fundusze.setText(jta_Fundusze.getText()+silnik.getGracze(x).getName()+": "+silnik.getGracze(x).getMoney()+"$\n");
            }
         jl_AktywnyGracz.setText("Aktywny Gracz: "+silnik.getAktywnyPlayer().getName()+" "+silnik.getAktywnyPlayer().getMoney()+"$");
    }//GEN-LAST:event_jb_RollActionPerformed

